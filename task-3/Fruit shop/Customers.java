class Customers {
	private int id;
	private String NameOfCustomer;

	public void setId(int id) {
	    this.id = id;
	}
	public int getId() {
	    return id;
	}
	public void setNameOfCustomer(String NameOfCustomer) {
	    this.NameOfCustomer = NameOfCustomer;
	}
	public String getNameOfCustomer() {
	    return NameOfCustomer;
	}
}